#include <unistd.h>

void	ft_putchar(char b)
{
	write(1, &b, 1);
}

void	ft_print_alphabet(void)
{
	char	x;

	x = 97;
	while (x <= 122)
	{
		ft_putchar(x);
		x++;
	}
}
