int	ft_is_prime(int nb)
{
	int	tut;
	int	i;

	i = 2;
	tut = 0;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	while (i * i <= nb)
	{
		if (nb % i == 0)
			tut++;
		i++;
	}
	if (tut == 0)
		return (1);
	return (0);
}
